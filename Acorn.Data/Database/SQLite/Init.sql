CREATE TABLE IF NOT EXISTS Accounts (
    Username TEXT NOT NULL,
    Password TEXT NOT NULL,
    FullName TEXT NOT NULL,
    Location TEXT NOT NULL,
    Email TEXT NOT NULL,
    Country TEXT NOT NULL,
    Created DATETIME NOT NULL,
    LastUsed DATETIME NOT NULL
);

